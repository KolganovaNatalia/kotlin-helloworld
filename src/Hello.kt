fun main(args: Array<String>){
    var five: Char = '5'
    println("5 to string: " + (5.toString()))
    println("5 to byte: " + (5.toByte()))
    println("5 to short: " + (5.toShort()))
    println("5 to int: " + (5.toInt()))
    println("5 to double: " + (5.toDouble()))
    println("5 to float: " + (5.toFloat()))
    println("5 to long: " + (5.toLong()))
}